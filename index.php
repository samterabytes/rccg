        <p style="text-shadow: unset;">Enoch Adejare Adeboye (born 2 March 1942 in Ifewara, Osun State, Nigeria) is a Nigerian pastor.

</p>
        <p style="text-align: justify;">After joining the RCCG in 1973, he began working to translate the sermons of its founder and then Pastor, Rev. Josiah Olufemi Akindayomi, from Yoruba into English.

Abeboye was ordained as a Pastor of the RCCG in 1977, and became General Overseer of the church in 1981. For three years he filled the role part-time, still lecturing at Ilorin, until giving up his university position to preach full-time.</p>
        <p>The church, which was not well known prior to Adeboye becoming the General Overseer, currently has several branches globally in about 196 nations (as at March 2017), including more than 14,000 in Nigeria. Adeboye has stated that his aim is to put a church within five minutes walking distance in developing cities and five minutes driving distance in developed cities. </p>
        <p>A recent regulation in Nigeria by the financial regulations council affecting all registered churches, mosques and Civil Society Organizations was put in place, placing a twenty-year cap on the leadership tenure of these organizations. Consequently, on January 7, 2017, Pastor J.O. Obayemi was appointed to head the church in Nigeria,while Pastor E. A. Adeboye still remains the General overseer of the Redeemed Christian Church of God (RCCG) globally.</p>
